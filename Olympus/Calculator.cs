namespace Olympus
{
    public class Calculator
    {
        public int AddNumbers(int a, int b)
        {
            return a + b;
        }

        public bool IsInputOddNumber(int a)
        {
            return (a % 2 != 0);
        }

        public double AddDoubleNumbers(double a, double b)
        {
            return a + b;
        }
    }
}
